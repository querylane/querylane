import { readFile, writeFile } from "node:fs/promises";

const specPath = new URL(
	"../docs/generated/querylane.openapi.yaml",
	import.meta.url,
);
const generatedHeader = "openapi: 3.1.0\ninfo: {}\n";
const publishedHeader = `# Generated by \`bun run docs:api:generate\`. Edit the protobuf source, not this file.
openapi: 3.1.0
info:
  title: Querylane experimental API
  version: v1alpha1
  description: >-
    Querylane's v1alpha1 ConnectRPC API, generated from its protobuf contract.
    It can change before a stable release. Querylane does not currently
    authenticate API callers; keep the server behind a trusted network or an
    authenticating reverse proxy.
`;

const generated = await readFile(specPath, "utf8");
if (!generated.startsWith(generatedHeader)) {
	throw new Error("generated OpenAPI header changed");
}

await writeFile(specPath, generated.replace(generatedHeader, publishedHeader));
